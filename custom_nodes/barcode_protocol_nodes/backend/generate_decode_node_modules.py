"""根据 Barcode 节点规格生成显式 decode 模块文件。"""

from __future__ import annotations

from pathlib import Path
from pprint import pformat

from custom_nodes.barcode_protocol_nodes.specs import (
    NODE_PACK_ID,
    NODE_PACK_VERSION,
    BarcodeNodeSpec,
    build_common_parameter_schema,
    build_decode_node_module_name,
    get_barcode_node_specs,
)


def _get_nodes_dir() -> Path:
    """返回 backend/nodes 目录绝对路径。"""

    return Path(__file__).resolve().parent / "nodes"


def _build_decode_node_definition_payload(spec: BarcodeNodeSpec) -> dict[str, object]:
    """构造单个 decode 节点的显式 NodeDefinition payload。

    参数：
    - spec：单个条码节点规格。

    返回：
    - dict[str, object]：节点定义 JSON payload。
    """

    return {
        "format_id": "amvision.node-definition.v1",
        "node_type_id": spec.node_type_id,
        "display_name": spec.display_name,
        "category": "barcode.decode",
        "description": spec.description,
        "implementation_kind": "custom-node",
        "runtime_kind": "python-callable",
        "input_ports": [
            {
                "name": "image",
                "display_name": "Image",
                "payload_type_id": "image-ref.v1",
            }
        ],
        "output_ports": [
            {
                "name": "results",
                "display_name": "Results",
                "payload_type_id": "barcode-results.v1",
            }
        ],
        "parameter_schema": build_common_parameter_schema(),
        "capability_tags": ["barcode.decode", f"barcode.{spec.capability_slug}"],
        "runtime_requirements": {"python_packages": ["opencv-python", "numpy", "zxing-cpp"]},
        "node_pack_id": NODE_PACK_ID,
        "node_pack_version": NODE_PACK_VERSION,
        "metadata": {
            "family": spec.family_key,
            "variant": spec.variant_key,
            "scope_kind": spec.scope_kind,
            "format_member_name": spec.format_member_name,
        },
    }


def _build_decode_module_content(spec: BarcodeNodeSpec) -> str:
    """构造单个 decode 节点模块内容。

    参数：
    - spec：单个条码节点规格。

    返回：
    - str：完整 Python 模块文本。
    """

    node_definition_payload = _build_decode_node_definition_payload(spec)
    return (
        f'"""{spec.display_name} 节点实现。"""\n\n'
        'from __future__ import annotations\n\n'
        'from custom_nodes.barcode_protocol_nodes.backend.support import build_decode_handler\n\n\n'
        f'NODE_TYPE_ID = "{spec.node_type_id}"\n\n\n'
        f'NODE_DEFINITION_PAYLOAD = {pformat(node_definition_payload, width=100, sort_dicts=False)}\n\n\n'
        f'handle_node = build_decode_handler(format_member_name="{spec.format_member_name}", requested_format="{spec.requested_format}")\n'
    )


def main() -> None:
    """按规格生成全部显式 decode 节点模块。"""

    nodes_dir = _get_nodes_dir()
    nodes_dir.mkdir(parents=True, exist_ok=True)
    for spec in get_barcode_node_specs():
        module_name = build_decode_node_module_name(spec.node_type_id)
        module_path = nodes_dir / f"{module_name}.py"
        module_path.write_text(_build_decode_module_content(spec), encoding="utf-8")


if __name__ == "__main__":
    main()
